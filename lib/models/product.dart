class Product {
  final String name;
  final String image;
  final String category;
  final String productType;
  final String size;
  final String quality;
  final String color;
  final String brand;
  final double price;

  Product({
    required this.name,
    required this.image,
    required this.category,
    required this.productType,
    required this.size,
    required this.quality,
    required this.color,
    required this.brand,
    required this.price,
  });
}